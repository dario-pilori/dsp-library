        while(PrimPol(i,a) ~= 100)
            a = a + 1;
        end
        for k = a:(a+length(tmp)-1)
            PrimPol(i,k) = str2num(tmp(k-a+1));
        end
    end
end

%% PRBS generator

data = NaN*zeros(num_PRBS,2^deg - 1);

% Initialize shift register

sr = ones(1,deg);

% implementation of the backward counter

pol_counter = num_pol;
PRBS_counter = 1;
flag = 0;

while(PRBS_counter <= num_PRBS)
    
    % check whether there are enough primitive polynomials different from all 0s
    
    if(pol_counter > num_row)
        flag = 1;
        pol_counter = 1;
    end
    
    if( flag == 1 && pol_counter == num_pol )
        data = NaN;
        disp('ERROR: not enough primitive polynomials (increase PRBS degree)')
        err_flag = 1;
        return;
    end

    % generating PRBS
    
    if(~isequal(PrimPol(pol_counter,:),zeros(1,length(PrimPol(pol_counter,:)))) )
        pos = find_ones(PrimPol(pol_counter,:));
        for i = 1:(2^(deg) - 1)
            data(PRBS_counter,i) = sr(end);
            tmp = sr(pos(1:(end-1)));
            tmp1 = sr(end);
            for j = length(tmp):-1:1
               tmp1 = xor(tmp1,tmp(j));
            end
            sr(2:end) = sr(1:(end-1));
            sr(1) = tmp1;
        end
        pol_counter = pol_counter + 1;
        PRBS_counter = PRBS_counter + 1;
    else
        pol_counter = pol_counter + 1;
    end    
end

% transposition of data for column notation

data = data';

end

function [ pos ] = find_ones( x )

%% This function returns the position of the ones inide the input vector

% Input:

% - x = input vector

% Output:

% pos = vector containing the positions of the value 1 in x

i = 1;
while(x(i) ~= 1)
    i = i + 1;
end

x1 = x(i:end);
j = 1;
for i = length(x1):-1:1
    x2(j) = x1(i);
    j = j + 1;
end

pos = zeros(1,length(x2(x2 == 1)) - 1);
k = 1;
for i = 2:length(x2)
    if(x2(i) == 1)
        pos(k) = i - 1;
        k = k + 1;
    end
end
end